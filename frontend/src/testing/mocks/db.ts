import { factory, primaryKey } from '@mswjs/data';
import { nanoid } from 'nanoid';

export const db = factory({
  package: {
    id: primaryKey(Number),
    type: String,
    title: String,
    description: String,
    price: Number,
    pricePerPrint: Number,
    imageSrc: String,
    isPopular: Boolean,
    durationSecs: Number,
    durationMins: Number,
  },
  session: {
    id: primaryKey(nanoid),
    packageId: Number,
    printCount: Number,
    basePrice: Number,
    voucherCode: String,
    discount: Number,
    finalPrice: Number,
    status: String,
    frameId: String,
    createdAt: String,
    expiresAt: String,
    completedAt: String,
  },
  transaction: {
    id: primaryKey(nanoid),
    sessionId: String,
    midtransOrderId: String,
    amount: Number,
    status: String,
    qrisUrl: String,
    qrisRawString: String,
    paidAt: String,
    createdAt: String,
  },
  voucher: {
    code: primaryKey(String),
    description: String,
    discountType: String,
    discountValue: Number,
    minPrice: Number,
    maxUses: Number,
    usedCount: Number,
    isActive: Boolean,
    expiresAt: String,
    createdAt: String,
  },
});
